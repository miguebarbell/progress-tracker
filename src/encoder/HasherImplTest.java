package encoder;

import org.junit.jupiter.api.Test;

class HasherImplTest {

	@Test
	void compare() {
		HasherImpl hasher = new HasherImpl();
		String root = "a00e4d3b352e9d11979549b9eef5dc951592f594488451e6cd86fdc4bce76a53";
		String miguel = "1a1524239dbdd240983104c353d508b08105e9176d95f128bf278d9a3b8635f4";
		String cognixia = "46a4765827d7cb97ecebb06d5b3cb1d7f146e03b42a097e361a19d6c425b34d0";
		assert hasher.compare("root", root);
		assert hasher.compare(miguel, "miguel");
		assert hasher.compare("cognixia", cognixia);
		assert !hasher.compare("m1guel", miguel);

	}

	@Test
	void hasher() {
		HasherImpl hasher = new HasherImpl();
		String progresstracker = hasher.hasher("progresstracker");
		assert ("4d08198f9d82f17046def6bd6072e05418a47301444aa767aa4e086fa954c3b7".equals(progresstracker));
	}
}
